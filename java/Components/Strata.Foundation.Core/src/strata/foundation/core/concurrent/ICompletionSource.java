package strata.foundation.core.concurrent;

public
interface ICompletionSource<T>
{
    T
    attach(ICompletionListener<T> listener);

    T
    detach(ICompletionListener<T> listener);

    boolean
    isAttached(ICompletionListener<T> listener);

    void
    complete();
}

